// ─── Scene Generator ──────────────────────────────────────────────────────

const RASHIS_SIMPLE = ['Mesha', 'Vrishabha', 'Mithuna', 'Karka', 'Simha', 'Kanya', 'Tula', 'Vrischika', 'Dhanu', 'Makara', 'Kumbha', 'Meena']

function generateScenes(kundli: KundliData): VideoScene[] {
  const lagna = RASHI_DATA[kundli.ascendant.rashiIndex]
  const nakshatra = NAKSHATRA_NAMES[kundli.ascendant.nakshatraIndex] ?? 'Unknown'
  const sun = kundli.planets.find(p => p.name === 'Sun')
  const moon = kundli.planets.find(p => p.name === 'Moon')
  const sunRashi = sun ? RASHIS_SIMPLE[sun.rashiIndex] : 'Unknown'
  const moonRashi = moon ? RASHIS_SIMPLE[moon.rashiIndex] : 'Unknown'

  const { currentMahadasha, currentAntardasha } = kundli.dashas
  const now = new Date()
  const currentYear = now.getFullYear()

  // Dignity summary
  const exalted = kundli.planets.filter(p => p.dignity === 'Exalted').map(p => p.name)
  const debilitated = kundli.planets.filter(p => p.dignity === 'Debilitated').map(p => p.name)
  const retrograde = kundli.planets.filter(p => p.isRetrograde).map(p => p.name)

  // Yoga highlights
  const topYogas = kundli.yogas.slice(0, 3)

  // Dosha warnings
  const doshas = kundli.doshas

  const scenes: VideoScene[] = []

  // Scene 1: Intro
  scenes.push({
    title: `${kundli.name}'s Kundli`,
    subtitle: 'Vedic Birth Chart Analysis',
    lines: [
      `${lagna?.symbol ?? '♈'} ${lagna?.name ?? ''} Lagna (${lagna?.western ?? ''})`,
      `Birth Nakshatra: ${nakshatra}`,
      `Sun: ${sunRashi} · Moon: ${moonRashi}`,
      `Generated by Nakshatra App`,
    ],
    icon: '⬡',
    duration: 8,
    color: '#FFB347',
  })

  // Scene 2: Planetary Overview
  const planetLines = kundli.planets.map(p => {
    const rashi = RASHIS_SIMPLE[p.rashiIndex]
    const ret = p.isRetrograde ? ' ℞' : ''
    const dig = p.dignity !== 'Neutral' ? ` (${p.dignity})` : ''
    return `${p.name}${ret}: ${rashi} · H${p.houseNumber}${dig}`
  })
  scenes.push({
    title: 'Planetary Positions',
    subtitle: 'The Nine Grahas at Birth',
    lines: planetLines,
    icon: '🪐',
    duration: 12,
    color: '#7DF9FF',
  })

  // Scene 3: Key Strengths
  const strengthLines: string[] = []
  if (exalted.length > 0) strengthLines.push(`Exalted: ${exalted.join(', ')} — peak power`)
  if (retrograde.length > 0) strengthLines.push(`Retrograde: ${retrograde.join(', ')} — deep inner work`)
  if (debilitated.length > 0) strengthLines.push(`Debilitated: ${debilitated.join(', ')} — growth areas`)

  const kendraP = kundli.planets.filter(p => [1, 4, 7, 10].includes(p.houseNumber))
  if (kendraP.length > 0) strengthLines.push(`Kendra planets: ${kendraP.map(p => p.name).join(', ')}`)

  const trikonaP = kundli.planets.filter(p => [1, 5, 9].includes(p.houseNumber))
  if (trikonaP.length > 0) strengthLines.push(`Trikona planets: ${trikonaP.map(p => p.name).join(', ')}`)

  scenes.push({
    title: 'Key Strengths',
    subtitle: 'Your Cosmic Advantages',
    lines: strengthLines.length > 0 ? strengthLines : ['Balanced chart with steady planetary energies'],
    icon: '💪',
    duration: 10,
    color: '#22c55e',
  })

  // Scene 4: Yogas
  if (topYogas.length > 0) {
    scenes.push({
      title: 'Yogas Detected',
      subtitle: `${kundli.yogas.length} yoga(s) found in your chart`,
      lines: topYogas.map(y => `${y.name} (${y.strength}) — ${y.description.slice(0, 80)}...`),
      icon: '✨',
      duration: 10,
      color: '#a855f7',
    })
  }

  // Scene 5: Current Dasha Period
  scenes.push({
    title: 'Current Dasha Period',
    subtitle: 'Your Cosmic Season Right Now',
    lines: [
      `Mahadasha: ${currentMahadasha.planet} (until ${new Date(currentMahadasha.endDate).getFullYear()})`,
      `Antardasha: ${currentAntardasha.planet}`,
      '',
      getDashaPrediction(currentMahadasha.planet),
    ],
    icon: '🔮',
    duration: 10,
    color: '#FFD700',
  })

  // Scene 6: Week & Month Ahead (transit-based)
  scenes.push({
    title: `${currentYear} Forecast`,
    subtitle: 'Week, Month & Year Outlook',
    lines: [
      `This Week: ${getWeekForecast(moonRashi)}`,
      '',
      `This Month: ${getMonthForecast(sunRashi, currentMahadasha.planet)}`,
      '',
      `Year Theme: ${getYearTheme(currentMahadasha.planet)}`,
    ],
    icon: '📅',
    duration: 12,
    color: '#3b82f6',
  })

  // Scene 7: Doshas & Remedies
  if (doshas.length > 0) {
    const doshaLines = doshas.flatMap(d => [
      `⚠ ${d.name} (${d.severity})`,
      `  ${d.description.slice(0, 80)}`,
    ])
    scenes.push({
      title: 'Doshas & Remedies',
      subtitle: 'Areas Needing Attention',
      lines: [...doshaLines, '', 'See Remedies page for detailed guidance'],
      icon: '🛡️',
      duration: 10,
      color: '#ef4444',
    })
  }

  // Scene 8: Remedies & Rituals
  const remedyLines = getRemedies(currentMahadasha.planet, doshas.map(d => d.name))
  scenes.push({
    title: 'Remedies & Rituals',
    subtitle: 'Strengthen Your Chart',
    lines: remedyLines,
    icon: '🙏',
    duration: 10,
    color: '#f97316',
  })

  // Scene 9: Positive Highlights
  scenes.push({
    title: 'The Good Parts',
    subtitle: 'Blessings in Your Chart',
    lines: getPositiveHighlights(kundli),
    icon: '🌟',
    duration: 10,
    color: '#eab308',
  })

  // Scene 10: Closing
  scenes.push({
    title: 'Hari Om',
    subtitle: `${kundli.name}, may the stars guide your path`,
    lines: [
      'This analysis covers your birth chart highlights,',
      'current dasha period, and near-term forecasts.',
      '',
      'For deeper insights, explore the full Kundli',
      'with Ashtakvarga, Shadbala, and Varshfal.',
      '',
      'Generated by Nakshatra · bitsizegyaan.com',
    ],
    icon: '🙏',
    duration: 8,
    color: '#FFB347',
  })

  return scenes
}

