function getPositiveHighlights(kundli: KundliData): string[] {
  const lines: string[] = []
  const exalted = kundli.planets.filter(p => p.dignity === 'Exalted')
  const ownSign = kundli.planets.filter(p => p.dignity === 'Own')

  if (exalted.length > 0) {
    lines.push(`${exalted.map(p => p.name).join(', ')} exalted — exceptional strength and blessings`)
  }
  if (ownSign.length > 0) {
    lines.push(`${ownSign.map(p => p.name).join(', ')} in own sign — natural confidence and ability`)
  }
  if (kundli.yogas.length > 0) {
    lines.push(`${kundli.yogas.length} yoga(s) detected — auspicious combinations active`)
    const strong = kundli.yogas.filter(y => y.strength === 'Excellent' || y.strength === 'Strong')
    if (strong.length > 0) {
      lines.push(`Top yoga: ${strong[0].name} — ${strong[0].description.slice(0, 60)}...`)
    }
  }

  const beneficsInKendra = kundli.planets.filter(
    p => ['Jupiter', 'Venus', 'Mercury'].includes(p.name) && [1, 4, 7, 10].includes(p.houseNumber)
  )
  if (beneficsInKendra.length > 0) {
    lines.push(`Benefics in Kendras: ${beneficsInKendra.map(p => p.name).join(', ')} — strong life support`)
  }

  if (lines.length === 0) {
    lines.push('A balanced chart with steady planetary energies')
    lines.push('Focus on your strengths and work on growth areas')
    lines.push('Regular remedies will enhance positive outcomes')
  }

  return lines
}

// ─── Canvas Video Renderer ────────────────────────────────────────────────

async function renderVideoToCanvas(
  canvas: HTMLCanvasElement,
  scenes: VideoScene[],
  onProgress: (pct: number) => void,
  onSceneChange: (idx: number) => void
): Promise<Blob> {
  const ctx = canvas.getContext('2d')!
  const W = canvas.width
  const H = canvas.height
  const FPS = 30
  const totalFrames = scenes.reduce((s, sc) => s + sc.duration * FPS, 0)

  // Use MediaRecorder to capture canvas as video
  const stream = canvas.captureStream(FPS)
  const chunks: Blob[] = []

  const mimeType = MediaRecorder.isTypeSupported('video/webm;codecs=vp9')
    ? 'video/webm;codecs=vp9'
    : MediaRecorder.isTypeSupported('video/webm')
    ? 'video/webm'
    : 'video/mp4'

  const recorder = new MediaRecorder(stream, { mimeType, videoBitsPerSecond: 2_000_000 })
  recorder.ondataavailable = (e) => { if (e.data.size > 0) chunks.push(e.data) }

  const done = new Promise<Blob>((resolve) => {
    recorder.onstop = () => {
      resolve(new Blob(chunks, { type: mimeType }))
    }
  })

  recorder.start()

  let frameCount = 0
  let sceneStartFrame = 0

  for (let si = 0; si < scenes.length; si++) {
    const scene = scenes[si]
    const sceneFrames = scene.duration * FPS
    onSceneChange(si)

    for (let f = 0; f < sceneFrames; f++) {
      const t = f / sceneFrames // 0 to 1 within scene

      // Clear
      const grad = ctx.createLinearGradient(0, 0, W, H)
      grad.addColorStop(0, '#0a0e27')
      grad.addColorStop(0.5, '#1a1040')
      grad.addColorStop(1, '#0a0e27')
      ctx.fillStyle = grad
      ctx.fillRect(0, 0, W, H)

      // Stars
      drawStarsOnCanvas(ctx, W, H, frameCount)

      // Scene fade in/out
      const fadeIn = Math.min(t * 5, 1)
      const fadeOut = Math.min((1 - t) * 5, 1)
      const alpha = Math.min(fadeIn, fadeOut)
      ctx.globalAlpha = alpha

      // Icon
      ctx.font = '48px serif'
      ctx.textAlign = 'center'
      ctx.fillStyle = scene.color
      ctx.fillText(scene.icon, W / 2, 80)

      // Title
      ctx.font = 'bold 28px serif'
      ctx.fillStyle = scene.color
      ctx.fillText(scene.title, W / 2, 130)

      // Subtitle
      if (scene.subtitle) {
        ctx.font = '16px sans-serif'
        ctx.fillStyle = 'rgba(255,255,255,0.5)'
        ctx.fillText(scene.subtitle, W / 2, 158)
      }

      // Content lines (animated reveal)
      ctx.font = '14px sans-serif'
      ctx.textAlign = 'left'
      const lineStart = 195
      const lineHeight = 22
      const revealedLines = Math.floor(t * scene.lines.length * 2)

      scene.lines.forEach((line, li) => {
        if (li > revealedLines) return
        const lineAlpha = Math.min((revealedLines - li) * 0.5, 1) * alpha
        ctx.fillStyle = `rgba(255,255,255,${lineAlpha * 0.8})`
        // Word wrap
        const maxWidth = W - 80
        const words = line.split(' ')
        let currentLine = ''
        let y = lineStart + li * lineHeight

        for (const word of words) {
          const testLine = currentLine ? `${currentLine} ${word}` : word
          if (ctx.measureText(testLine).width > maxWidth && currentLine) {
            ctx.fillText(currentLine, 40, y)
            currentLine = word
            y += lineHeight
          } else {
            currentLine = testLine
          }
        }
        if (currentLine) ctx.fillText(currentLine, 40, y)
      })

      // Progress bar at bottom
      ctx.globalAlpha = 0.3
      ctx.fillStyle = '#333'
      ctx.fillRect(20, H - 30, W - 40, 4)
      ctx.fillStyle = scene.color
      ctx.globalAlpha = 0.8
      ctx.fillRect(20, H - 30, (W - 40) * (frameCount / totalFrames), 4)

      // Branding
      ctx.globalAlpha = 0.3
      ctx.fillStyle = '#FFB347'
      ctx.font = '10px sans-serif'
      ctx.textAlign = 'center'
      ctx.fillText('Nakshatra · Vedic Astrology App', W / 2, H - 10)

      ctx.globalAlpha = 1
      frameCount++
      onProgress(frameCount / totalFrames)

      // Yield to browser to keep UI responsive
      if (f % 3 === 0) {
        await new Promise(r => setTimeout(r, 0))
      }
    }
    sceneStartFrame += sceneFrames
  }

  recorder.stop()
  return done
}

function drawStarsOnCanvas(ctx: CanvasRenderingContext2D, w: number, h: number, frame: number) {
  const rng = (seed: number) => {
    let s = seed
    return () => { s = (s * 16807) % 2147483647; return s / 2147483647 }
  }
  const rand = rng(42)
  ctx.fillStyle = 'rgba(255,255,255,0.3)'
  for (let i = 0; i < 40; i++) {
    const x = rand() * w
    const y = rand() * h
    const r = rand() * 1.2
    const twinkle = Math.sin(frame * 0.05 + i) * 0.5 + 0.5
    ctx.globalAlpha = twinkle * 0.4
    ctx.beginPath()
    ctx.arc(x, y, r, 0, Math.PI * 2)
    ctx.fill()
  }
  ctx.globalAlpha = 1
}

// ─── Sharing Utilities ────────────────────────────────────────────────────

async function shareViaWhatsApp(blob: Blob, name: string) {
  const file = new File([blob], `${name}-kundli-analysis.webm`, { type: blob.type })
  if (typeof navigator.canShare === 'function' && navigator.canShare({ files: [file] })) {
    await navigator.share({
      title: `${name}'s Kundli Analysis`,
      text: 'Watch my Vedic birth chart analysis from Nakshatra App',
      files: [file],
    })
  } else {
    // Fallback: download
    downloadBlob(blob, `${name}-kundli-analysis.webm`)
  }
}

async function shareViaEmail(blob: Blob, name: string) {
  // Cannot attach files via mailto, so download + open mailto
  downloadBlob(blob, `${name}-kundli-analysis.webm`)
  const subject = encodeURIComponent(`${name}'s Vedic Kundli Analysis`)
  const body = encodeURIComponent(`Hi,\n\nPlease find my Vedic birth chart analysis attached.\n\nGenerated by Nakshatra App - bitsizegyaan.com`)
  window.open(`mailto:?subject=${subject}&body=${body}`, '_blank')
}

function downloadBlob(blob: Blob, filename: string) {
  const url = URL.createObjectURL(blob)
  const a = document.createElement('a')
  a.href = url
  a.download = filename
  document.body.appendChild(a)
  a.click()
  document.body.removeChild(a)
  URL.revokeObjectURL(url)
}

// ─── Preview Player ───────────────────────────────────────────────────────

function VideoPreview({ scenes, currentScene }: { scenes: VideoScene[]; currentScene: number }) {
  const scene = scenes[currentScene]
  if (!scene) return null

  return (
    <div className="aspect-video bg-cosmos rounded-xl border border-stardust/30 overflow-hidden relative">
      <div className="absolute inset-0 flex flex-col items-center justify-center p-6 text-center">
        <motion.div
          key={currentScene}
          initial={{ opacity: 0, y: 20 }}
          animate={{ opacity: 1, y: 0 }}
          exit={{ opacity: 0, y: -20 }}
          className="space-y-3"
        >
          <span className="text-4xl">{scene.icon}</span>
          <h3 className="font-cinzel text-lg" style={{ color: scene.color }}>{scene.title}</h3>
          {scene.subtitle && <p className="text-xs text-white/40 font-cormorant">{scene.subtitle}</p>}
          <div className="text-left max-w-sm mx-auto">
            {scene.lines.slice(0, 4).map((line, i) => (
              <motion.p
                key={i}
                initial={{ opacity: 0, x: -10 }}
                animate={{ opacity: 1, x: 0 }}
                transition={{ delay: i * 0.15 }}
                className="text-xs text-white/60 font-cormorant leading-relaxed"
              >{line}</motion.p>
            ))}
            {scene.lines.length > 4 && <p className="text-xs text-white/30 font-cormorant">...</p>}
          </div>
        </motion.div>
      </div>

      {/* Scene progress dots */}
      <div className="absolute bottom-3 left-0 right-0 flex justify-center gap-1">
        {scenes.map((_, i) => (
          <div key={i} className={`w-1.5 h-1.5 rounded-full transition-all ${i === currentScene ? 'bg-gold' : i < currentScene ? 'bg-gold/30' : 'bg-white/10'}`} />
        ))}
      </div>
    </div>
  )
}

